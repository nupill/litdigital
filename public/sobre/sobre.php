	<h3>Sobre a Biblioteca de Literaturas de Língua Portuguesa</h3>
	<br />
	
	<p>Nossa Biblioteca Digital é uma das poucas no Brasil que é fonte primária e gratuita
	de textos literários em versão integral na internet. Trata-se de obras do Brasil e de
	Portugal, a partir das melhores edições disponíveis.</p>
	
	<p>Projeto nascido no NUPILL – Núcleo de Pesquisa em Informática, Literatura e
	Linguística e no LAPESD – Laboratório de Pesquisa em Sistemas Distribuídos, a
	Biblioteca Digital de Literaturas de Língua Portuguesa é a maior biblioteca digital
	de literatura brasileira e portuguesa que existe, aberta a usuários de interesses bem
	variados, do leitor diletante ao pesquisador especialista.</p>
	
	<p>Além da consulta às obras digitalizadas, é possível acessar um catálogo com dados
	biobibliográficos dos autores brasileiros e portugueses; também estão disponíveis
	documentos do acervo pessoal de alguns autores do Estado de Santa Catarina. Nosso
	acervo é uma rica fonte de pesquisa sobre história literária, sendo possível realizar
	pesquisas a respeito dos autores, das obras publicadas, datas de publicação, editoras,
	gênero das obras, entre outras. É importante ressaltar que as obras digitalizadas são
	em menor número do que aquelas cujas informações estão também disponíveis.</p>
	
	<p>Nossa proposta é criar um ambiente de leitura e ensino-aprendizagem de literatura,
	a partir do aproveitamento da biblioteca digital, incluindo ferramenta de anotações.
	Qualquer usuário pode utilizar a Biblioteca e o Banco de Dados Digital, sem
	necessidade de inscrever-se, nem de entrar com nome e senha. Contudo, para
	aqueles que se cadastram como usuário e entram no sistema com nome e senha,
	abre-se a possibilidade de fazer anotações nas obras digitalizadas que for lendo. Em
	qualquer lugar do mundo, a qualquer momento, esse leitor, ao entrar na Biblioteca
	com seu nome e sua senha, terá acesso às anotações que já fez e poderá realizar
	outras.</p>
	
	<p>A Biblioteca Digital é uma atividade incessante, ou seja, estamos frequentemente
	acrescentando e corrigindo dados, pois eles são sempre sujeitos a omissões e a
	incorreções. Portanto, gostaríamos de pedir a colaboração de todos os usuários para o
	aperfeiçoamento dos dados, enviando-nos todas as dúvidas e sugestões que acharem
	relevantes (<span class="email">nupill.ufsc(arroba)gmail.com</span>).</p>
	
